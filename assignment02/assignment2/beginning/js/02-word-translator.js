let lang = prompt('Select the language code es, de, en or fr');
if (lang === 'es') {
    document.write('Bueno El Mundo');
} else if (lang === 'de') {
    document.write('Hallo Welt');
} else if (lang === 'en') {
    document.write('Hello World');
} else if (lang === 'fr') {
    document.write('Bonjour le monde');
} else {
    document.write('Hello World');
}


